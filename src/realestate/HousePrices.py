main_file_path = 'U:/tools/kaggle/houseprice/train.csv' # this is the path to the Iowa data that you will use
df_train = pd.read_csv(main_file_path)


corrmat = df_train.corr()
f, ax = plt.subplots(figsize=(12, 9))
